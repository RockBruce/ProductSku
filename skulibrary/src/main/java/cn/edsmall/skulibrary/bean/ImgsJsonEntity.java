package cn.edsmall.skulibrary.bean;

public class ImgsJsonEntity {
    private String higImg;
    private String supMainImgs;
    private String detailImgs;
    private String video;
    private String model3D;

    public String getHigImg() {
        return higImg;
    }

    public void setHigImg(String higImg) {
        this.higImg = higImg;
    }

    public String getSupMainImgs() {
        return supMainImgs;
    }

    public void setSupMainImgs(String supMainImgs) {
        this.supMainImgs = supMainImgs;
    }

    public String getDetailImgs() {
        return detailImgs;
    }

    public void setDetailImgs(String detailImgs) {
        this.detailImgs = detailImgs;
    }

    public String getVideo() {
        return video;
    }

    public void setVideo(String video) {
        this.video = video;
    }

    public String getModel3D() {
        return model3D;
    }

    public void setModel3D(String model3D) {
        this.model3D = model3D;
    }
}
